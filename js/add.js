//Arithmetic
let a = 5;
let b = 10;
let sum = a + b;
console.log(sum);

//Functions
function add(a, b) {
    return a + b;
}
console.log(add(5, 10));

//Conditionals If-Else
let number = 10;
if (number > 5) {
    console.log("Greater than 5");
} else {
    console.log("5 or less");
}

//Loops
for (let i = 0; i < 5; i++) {
    console.log(i);
}

//Data Structures (Arrays/Lists)
let numbers = [1, 2, 3, 4, 5];
numbers.forEach(number => console.log(number));

