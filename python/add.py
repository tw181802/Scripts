#Arithmetic
a = 5
b = 10
sum = a + b
print(sum)

#Functions
def add(a, b):
    return a + b
print(add(5, 10))


#Conditionals If-Else
number = 10
if number > 5:
    print("Greater than 5")
else:
    print("5 or less")


#Loops
for i in range(5):
    print(i)



#Data Structures (Arrays & Lists)
numbers = [1, 2, 3, 4, 5]
for number in numbers:
    print(number)

